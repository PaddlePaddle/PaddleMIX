@paddle.no_grad()
def text2image_ldm_stable(
    model,
    prompt: List[str],
    controller,
    num_inference_steps: int = 50,
    guidance_scale: Optional[float] = 7.5,
    generator: Optional[paddle.fluid.libpaddle.Generator] = None,
    latent: Optional[paddle.Tensor] = None,
    uncond_embeddings=None,
    start_time=50,
    return_type="image",
):
    batch_size = len(prompt)
    ptp_utils.register_attention_control(model, controller)
    height = width = 512

    text_input = model.tokenizer(
        prompt,
        padding="max_length",
        max_length=model.tokenizer.model_max_length,
        truncation=True,
        return_tensors="pd",
    )
    text_embeddings = model.text_encoder(text_input.input_ids)[0]
    max_length = text_input.input_ids.shape[-1]
    if uncond_embeddings is None:
        uncond_input = model.tokenizer(
            [""] * batch_size, padding="max_length", max_length=max_length, return_tensors="pd"
        )
        uncond_embeddings_ = model.text_encoder(uncond_input.input_ids)[0]
    else:
        uncond_embeddings_ = None

    latent, latents = ptp_utils.init_latent(latent, model, height, width, generator, batch_size)
    model.scheduler.set_timesteps(num_inference_steps)
    for i, t in enumerate(tqdm(model.scheduler.timesteps[-start_time:])):
        if uncond_embeddings_ is None:
            context = paddle.concat([uncond_embeddings[i].expand(text_embeddings.shape), text_embeddings])
        else:
            context = paddle.concat([uncond_embeddings_, text_embeddings])
        latents = ptp_utils.diffusion_step(model, controller, latents, context, t, guidance_scale, low_resource=False)

    if return_type == "image":
        image = ptp_utils.latent2image(model.vae, latents)
    else:
        image = latents
    return image, latent


def run_and_display(
    prompts, controller, latent=None, run_baseline=False, generator=None, uncond_embeddings=None, verbose=True
):
    if run_baseline:
        print("w.o. prompt-to-prompt")
        images, latent = run_and_display(
            prompts, EmptyControl(), latent=latent, run_baseline=False, generator=generator
        )
        print("with prompt-to-prompt")
    images, x_t = text2image_ldm_stable(
        ldm_stable,
        prompts,
        controller,
        latent=latent,
        num_inference_steps=NUM_DDIM_STEPS,
        guidance_scale=GUIDANCE_SCALE,
        generator=generator,
        uncond_embeddings=uncond_embeddings,
    )
    if verbose:
        ptp_utils.view_images(images)
    return images, x_t


